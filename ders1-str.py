#str():Verilen parametreyi karakter tipine dönüştürmeyi sağlar.
# yorumlayıcıda çalışırken ayrı sayfada çalıştırırken print ile kullanın.
print(str('Bilgisayar Bilimi'))# parametre karakter veri türünde
print(str(553))   # parametre tamsayı türünde
print(str(6.61))  # parametre ondalıklı sayı türünde
print(str(False)) # parametre mantıksal bir ifade
print("5"+"10")   # iki karakter yan yana yazılır -->510
print(str(5)+str(10))# 5 ve 10 tamsayıları karaktere çevrilir -->510 yazar
