#float(): Verilen parametreyi ondalıklı sayıya çevirmeyi sağlar
print(float("3.14"))# 3.14 yazar   karakter veri tipi -> ondalıklı veri tipi
print(float(2.71))  # 2.71 yazar   ondalıklı veri tipi-> ondalıklı veri tipi
print(float(5))     # 5.0  yazar   tamsayı veri tipi  -> ondalıklı veri tipi
print(float(False)) # 0.0  yazar   mantıksal veri tipi-> ondalıklı veri tipi
